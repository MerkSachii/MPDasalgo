package edu.dasalgo.java;

public class Person {
	
	private int weight;
	private int patienceLevel;
	private boolean toStairs;
	private double timeToVacate;
	
//	
//	Constructor
	
	public Person() {
		
	}
//	
//

//	
//	GETTERS
	public int getWeight() {
		return weight;
	}

	public void setWeight(int weight) {
		this.weight = weight;
	}

	public int getPatienceLevel() {
		return patienceLevel;
	}

	public void setPatienceLevel(int patienceLevel) {
		this.patienceLevel = patienceLevel;
	}

//
//	GETTERS ends here
	
//	
//	SETTERS
	public boolean isToStairs() {
		return toStairs;
	}

	public void setToStairs(boolean toStairs) {
		this.toStairs = toStairs;
	}

	public double getTimeToVacate() {
		return timeToVacate;
	}

	public void setTimeToVacate(double timeToVacate) {
		this.timeToVacate = timeToVacate;
	}
	
//	
//	SETTERS ensd here
	

}
